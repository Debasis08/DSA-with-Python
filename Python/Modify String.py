# For Upper Case
a = "Hello, World!"
print(a.upper())


# For Lower Case
print(a.lower())


# Remove unnecessary white-space before and after the text/string
print(a.strip())


# Replacement of a String
print(a.replace("World", "Guys"))
# it will replace the letter/s of first "()" with the 2nd "()"


# Split String
print(a.split("l"))    # ['He', '', 'o, Wor', 'd!']
print(a.split(","))    # ['Hello', ' World!']