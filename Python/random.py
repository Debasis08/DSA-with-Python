# Python does not have a random() function to make a random number,
# but Python has a built-in module called "random" that can be used
# to make random numbers:

# So, here's how the code looks like:

import random

print(random.randrange(1,10))

# any random number between two numbers that have been mentioned
# in the parenthesis will be generated through the built-in-module

#Run the Code and Check the output by typing "python random.py" in the terminal